package day02;
//논리 연산좌 '3' && ||

public class operatorEx6 {
	public static void main(String[] args) {
		boolean b1 = true;
		boolean b2 = false;
		
		System.out.println("b1 && b2  " + (b1 && b2));
		System.out.println("b1 || b2  " + (b1 || b2));
		
			
		// 참 거짓으루 연산하는 애들읨
		
	}//main method
}//class end
